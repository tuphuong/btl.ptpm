    private boolean validForm() {
        if (!Validation.isEmpty(txt_Danop, "Số tiền đã nộp không được trống!")) {
            return false;
        }
        if (!Validation.isNumber(txt_Danop, "Số tiền đã nộp phải là số!")) {
            return false;
        }
        if (!Validation.checkGiaTien(txt_Danop, "Số tiền đã nộp phải >= 1.000 VNĐ!")) {
            return false;
        }       
        if (!Validation.isEmpty(txt_Nguoinop, "Người nộp không được trống!")) {
            return false;
        }
        if (!Validation.isEmptyDate(jDateChooser, "Ngày nộp không được trống!")) {
            return false;
        }
        return true;
    }
