    private void btn_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SubmitActionPerformed
        // TODO add your handling code here:
        HocPhi hocphi = new HocPhi();
        hocPhiController = new HocPhiController();
        hocSinhController = new HocSinhController();
        hocphi.setMaHV(txt_MaHV.getText());
        hocphi.setMaLop(hocSinhController.getMaLopByMaHV(txt_MaHV.getText()).getMaLop());

        if (validForm()) {
            hocphi.setDanop(Integer.parseInt(txt_Danop.getText()));
            hocphi.setConthieu(Integer.parseInt(txt_Phainop2.getText()) - Integer.parseInt(txt_Danop.getText()));
            txt_Conthieu.setText(String.valueOf(hocphi.getConthieu()));
            hocphi.setNgaynop(jDateChooser.getDate());
            if (jDateChooser.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Ngày nộp không được trống");
            } else {
                hocphi.setNguoinop(txt_Nguoinop.getText());
                
                int check = hocPhiController.Update(hocphi);
                if (check == 1) {
                    JOptionPane.showMessageDialog(this, "Sửa học phí thành công");
                } else {
                    hocPhiController.Add(hocphi);
                    JOptionPane.showMessageDialog(this, "Nộp học phí thành công");
                    
                }
            }

        } else {
            return;
        }
    }//GEN-LAST:event_btn_SubmitActionPerformed

    private void HienThiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HienThiActionPerformed
        // TODO add your handling code here:
        int row = table_Data_NopTien.getSelectedRow();
        if (row != -1) {
            HocPhi hp = new HocPhi();
            List<HocPhi> ListHocPhi = new ArrayList<>();

            hocPhiController = new HocPhiController();
            lopHocController = new LopHocController();
            String MaHV = String.valueOf(table_Data_NopTien.getValueAt(row, 1));
            String TenHV = String.valueOf(table_Data_NopTien.getValueAt(row, 2));
            String TenLop = String.valueOf(table_Data_NopTien.getValueAt(row, 3));

            lop = lopHocController.getClassByName(TenLop);
            hp = hocPhiController.getStudentByName(TenHV);
            ListHocPhi = hocPhiController.getHocPhiByMaHV(MaHV);

            txt_MaHV.setText(MaHV);
            txt_tenHS.setText(TenHV);
            txt_tenLop2.setText(TenLop);
            txt_Giatien2.setText(String.valueOf(lop.getGiatien()));
            txt_Phainop2.setText(String.valueOf(lop.getPhainop()));
            
            if (ListHocPhi.size() == 0) {
                txt_Danop.setText("");
                txt_Conthieu.setText("");
                jDateChooser.setDate(null);
                txt_Nguoinop.setText("");
            } else {
                txt_Danop.setText(String.valueOf(hp.getDanop()));
                txt_Conthieu.setText(String.valueOf(hp.getPhainop() - hp.getDanop()));
                jDateChooser.setDate(hp.getNgaynop());
                txt_Nguoinop.setText(hp.getNguoinop());
            }

        } else {
            JOptionPane.showMessageDialog(this, "Chưa chọn học sinh!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_HienThiActionPerformed

