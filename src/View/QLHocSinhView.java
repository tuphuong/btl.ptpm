    private boolean validForm() {
        String MaHV = txt_maHS.getText();
        String MaLop = txt_maLop.getText();
        HocSinh check = HocSinhController.getHocSinhByMaHV(MaHV);
        LopHoc checkMaLop = HocSinhController.checkMaLop(MaLop);
        if (!Validation.isEmpty(txt_maHS, "Mã học viên không được trống!")) {
            return false;
        }
        if (!Validation.checkMaHV(txt_maHS, "Mã học viên sai định dạng!")) {
            return false;
        }
        if (check != null) {
            JOptionPane.showMessageDialog(null, "Mã học viên đã tồn tại");
            txt_maHS.setBackground(Color.yellow);
            txt_maHS.requestFocus();
            return false;
        }
        if (!Validation.isEmpty(txt_hoTen, "Họ tên học viên không được trống!")) {
            return false;
        }
        if (jDateChooser.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không được trống");
            return false;
        }
        if (!Validation.isEmpty(txt_tenPH, "Tên phụ huynh không được trống!")) {
            return false;
        }
        if (!Validation.isEmpty(txt_soDienThoai, "Số điện thoại không được trống!")) {
            return false;
        }
        if (!Validation.checkSDT(txt_soDienThoai, "Số điện thoại chưa đúng định dạng!")) {
            return false;
        }
        if (!Validation.isEmpty(txt_diaChi, "Địa chỉ không được trống!")) {
            return false;
        }
        if (!Validation.isEmpty(txt_maLop, "Mã lớp không được trống!")) {
            return false;
        }
        if (checkMaLop == null) {
            JOptionPane.showMessageDialog(null, "Lớp học chưa tồn tại");
            txt_maLop.setBackground(Color.yellow);
            txt_maLop.requestFocus();
            return false;
        }

        return true;
    }

