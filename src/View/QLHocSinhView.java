            hs.setMaHV(MaHV);
            hs.setHoTen(HoTen);
            hs.setGioiTinh(GioiTinh);
            hs.setNgaySinh(NgaySinh);
            hs.setSdtPH(SdtPH);
            hs.setTenPH(TenPH);
            hs.setDiaChi(DiaChi);
            hs.setMaLop(MaLop);

            HocSinhController.Add(hs);
            lopHocController.updateNumberOfClass(MaLop, "Thêm");
