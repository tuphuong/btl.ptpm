        LopHoc lop = new LopHoc();
        String MaHV = txt_maHS.getText();
        String HoTen = txt_hoTen.getText();
        String GioiTinh = txt_gioiTinh.getSelectedItem().toString();
        Date NgaySinh = jDateChooser.getDate();
