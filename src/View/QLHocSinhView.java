    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:
        int row = table_Data_HS.getSelectedRow();
        if (row == -1) {//row =-1 là người dùng chưa chọn hàng nào trong table
            JOptionPane.showMessageDialog(QLHocSinhView.this, "Vui lòng chọn một học viên", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(QLHocSinhView.this, "Bạn có chắc chắn xóa không?");

            if (confirm == JOptionPane.YES_OPTION) {
                String MaHV = String.valueOf(table_Data_HS.getValueAt(row, 0));
                HocSinhController.Delete(MaHV);
                String MaLH = String.valueOf(table_Data_HS.getValueAt(row, 7));
                lopHocController.updateNumberOfClass(MaLH, "Xóa");

                defaultTableModel.setRowCount(0);//Xóa dữ liệu cũ

                setTableData(HocSinhController.getAllHocSinhs());
            }
        }
    }//GEN-LAST:event_btn_XoaActionPerformed
    private void setTableData(List<HocSinh> hocsinh) {

        for (HocSinh hocSinh : hocsinh) {
            defaultTableModel.addRow(new Object[]{hocSinh.getMaHV(), hocSinh.getHoTen(), hocSinh.getGioiTinh(),
                hocSinh.getNgaySinh(), hocSinh.getSdtPH(), hocSinh.getTenPH(), hocSinh.getDiaChi(), hocSinh.getMaLop()});
        }
    }
